<p>
ADM ve GDZ Elektrik Dağıtım şirketlerinin, tüm insan kaynakları iş süreçlerini yeniden tasarlayıp, dijitalleştirerek gerçekleştirdiği yönetim sistemi, SAP NOW İstanbul’da “Yılın İnsan Kaynakları” ödülüne layık görüldü.</p>

<p>ADM ve GDZ Elektrik Dağıtım İcra Kurulu Başkanı Uğur Yüksel: “Tüm çalışanlar şirket hedeflerini benimsediğinde başarı gelecektir”.</p>
<p>Elektrik dağıtım sektöründe dönüşüm tüm hızıyla sürüyor. Özellikle insan kaynaklarında evrensel standartları benimsemeye yönelik çalışmalar hız kazanırken, dijitalleşme ile iş süreçlerinin sadeleşmesi sağlanıyor.</p>

<p>ADM ve GDZ Elektrik Dağıtım İcra Kurulu Başkanı Uğur Yüksel, tüm çalışanların şirket hedeflerini benimsediğinde performansın arttığına dikkat çekerek, “İş süreçlerini sadeleştirmek, dijitalleşmenin gereği. ADM ve GDZ Elektrik Dağıtım olarak bu süreçleri en iyi şekilde uyguladığımıza inanıyoruz” dedi.</p>

<p>ADM ve GDZ Elektrik Dağıtım şirketlerinin Türkiye’nin en önemli teknoloji etkinliklerinden biri olan SAP NOW İstanbul’da “Yılın İnsan Kaynakları” ödülü almasına yönelik değerlendirmede bulunan Yüksel, iş süreçlerini yazılım ve iş uygulamalarıyla dijitalleştirdiklerine dikkat çekerek şunları söyledi:

“İş süreçlerini etkin biçimde yöneten, uygulamada mükemmelliği yakalayan bir kurum olma hedefine hızla ilerliyoruz. Fark yaratan teknoloji uygulamaları, elde edilen fayda, maliyet, hız ve sağlanan katma değer gibi kriterleri gözeterek çalışıyoruz. Şirket içindeki tüm süreçleri bir araya getiren ve birlikte ilerlemesini sağlayan bir çözüm olduğu için tercih edilen SAP SuccessFactors ile “Performans Yönetim Sistemi”nin süreç ve iş akışlarını dijitale dönüştürerek kurguladık. ADM ve GDZ Elektrik Dağıtım şirketleri, tüm insan kaynakları iş süreçlerini yeniden tasarlayıp, dijitalleştirerek bu başarıya imza attı.”</p>

<p>Şirketlerin sektörlerindeki global standartlarla kendilerini kıyaslama fırsatı yakaladığına değinen Uğur Yüksel, dijital çözümler ile tüm çalışanların, şirket hedeflerini benimsemesini ve istenen organizasyonel sonuçların elde edilmesinin sağlanacağını belirtti.</p>


