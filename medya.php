<p><font size="5" color="#666666"><b><div align="center">ADM Elektrik Dağıtım Seçim Hazırlıklarını Tamamladı</div></b></font>
</p>
<p>4 Haziran’da yapılacak Cumhurbaşkanlığı ve 27. Dönem Milletvekili Genel Seçimi’nde oyların kullanılacağı, sayım ve dökümünün yapılacağı yerlerde işlemlerin geç saatlere kadar sürebileceğini göz önüne alan ADM Elektrik Dağıtım; hizmet bölgesi olan 3 ilde kaliteli ve kesintisiz enerji için hazırlıklarını tamamladı.</p>

<p>ADM Elektrik Dağıtım, seçimler süresince Aydın, Denizli ve Muğla illerine kesintisiz hizmet için ek tedbirler aldı. 3 ilde 48 ilçeye, yaklaşık 65.000 km hat ve 20.000 adet trafo ile elektrik dağıtımı yapan şirket, yaklaşık 700 kişilik saha çalışanı ve 121 adet araç ile seçim günü 24 saat kesintisiz mesai yapacak.</p>

<p>Seçim Öncesi Planlı Kesinti Yapılmayacak</p>

<p>Bakım, onarım ve yatırım çalışmalarının devreye alınması amacıyla yapılan planlı kesintiler, seçim öncesinde yapılmayacak. Şirket; enerji sürekliliğini sağlamak amacıyla elektrik hatlarının uygun olduğu yerlerde alternatif enerji besleme planları da yaptı.</p>

<p>İlave Ekip Organizasyonu Yapıldı</p>

<p>Seçime özel hazırlıklar kapsamında Aydın, Denizli ve Muğla illeri ve bağlı ilçelerde nöbetçi ekip amirleri görevlendirildi. Seçim günü boyunca çalışanların izinleri kaldırılırken, takviye ekipler de oluşturuldu.</p>

<p>Paydaşlarla Koordinasyon İçinde Çalışacaklar</p>

<p>İl ve ilçelerdeki paydaşlarıyla Seçim Güvenlik Toplantıları’nda sık sık bir araya geldiklerini belirten şirket yetkilileri, 24 Haziran tarihinde sabahın erken saatlerinden, oy sayımının tamamlanacağı geç vakitlere kadar görev başında olacaklarını söyledi.</p>

<p>Çağrı Merkezi Yoğun Mesaiye Hazır</p>

<p>7 / 24 kesintisiz hizmet için 400 kişilik uzman ve deneyimli kadrosu ile çalışan şirketin çağrı merkezi, seçim gününe hazır. Tüketicilerin telefon aracılığıyla ilettiği ihbarların yanında; şirketin kurumsal sosyal medya hesapları ve ücretsiz mobil uygulaması ile ulaşan talepler de hızlı bir şekilde işleme alınacak.</p>
