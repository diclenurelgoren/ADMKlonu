<p><font size="5" color="#666666" ><b><div align="center">ADM ve GDZ Elektrik Dağıtım Uğur Yüksel’le Güç Kazandı</div></b></font>
</p>
<p>Aydın, Denizli, Muğla, İzmir ve Manisa illerinde elektrik dağıtım hizmeti veren ADM ve GDZ Elektrik Dağıtım şirketlerinin İcra Kurulu Başkanı Uğur Yüksel oldu.</p>
<p>Eylül 2011’den bu yana, elektrik dağıtım sektörünü temsil eden ELDER - Elektrik Dağıtım Hizmetleri Derneği’nin Genel Sekreterliği’ni yürüten Uğur Yüksel, 8 Ağustos tarihinde ADM ve GDZ Elektrik Dağıtım şirketlerinde Yönetim Kurulu Üyesi ve İcra Kurulu Başkanı olarak göreve başladı.</p>

<p>Enerji, bilişim ve savunma sanayii sektörlerinde 30 yılı aşan deneyime sahip olan Yüksel, iş hayatına 1987 yılında Milli Savunma Bakanlığı Savunma Sanayi Müsteşarlığında başladı ve savunma sanayiinde ileri teknoloji geliştirilmesine yönelik projelerde Proje Yöneticisi olarak yer aldı.</p>

<p>2001’de Türkiye’nin ilk tekno parkı olan ODTÜ Teknokent ‘in kurucu genel müdürlüğünü üstlenen Yüksel, 2008- 2011 yılları arasında, Lotus Enerji’de Türkmenistan’da Enerji Santrali, iletim ve dağıtım hattı projelerinin yürütülmesinde Satınalma ve İş Geliştirme Direktörü olarak görev yaptı.</p>

<p>Lisans eğitimini Hacettepe Üniversitesi Elektrik Mühendisliği’nde tamamlayan Yüksel, Drexel Üniversitesi’nden (ABD) işletme yüksek lisans derecesine sahiptir.</p>

<p>5 milyon tüketiciye hizmet veren ADM ve GDZ Elektrik Dağıtım şirketleri Yüksel’in tecrübeleri ile Türkiye için stratejik öneme sahip olan elektrik dağıtım sektörüne önemli katkılar sağlayacak ve başarılı performansları katlanarak artacaktır.</p>
