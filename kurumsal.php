<p>Göçmen kuşların göç yolları üzerinde yer alan hizmet bölgesinde, elektrik direklerinin tepe noktalarına yuva yapmaya elverişli platformlar yerleştirilerek leyleklerin zarar görmesi engelleniyor. Büyük bir farkındalığın ilk adımı olan proje, her yıl yeni tespit edilen noktalar ve özellikle tüketicilerin yönlendirmeleri ile belirlenen lokasyonlarda devam ediyor.</p>

 

<p>“Enerji Avcıları” Projesi</p>

<p>Hizmet bölgesinde yaklaşık 3 milyon kişinin hayatını aydınlatan Şirketimiz, gelecek neslin enerji verimliliği konusunda farkındalık kazanması ve bu bilincin bir davranış biçimi haline gelmesi amacıyla enerji verimliliği eğitim çalışmalarına bu yıl “Enerji Avcıları” isimli proje ile devam ediyor.</p> 

<p>Çevreye, doğaya ve topluma karşı olan sosyal sorumluluk bilinciyle; görevi devraldığı 2008 yılından itibaren gerçekleştirdiği Enerji Verimliliği Haftası etkinliklerini, yıl boyu süren Kurumsal Sosyal Sorumluluk projesine dönüştürdü. </p>

<p>11 – 18 Ocak Enerji Verimliliği Haftası’nın başlaması ile proje faaliyetlerini yoğunlaştıran şirketin deneyimli eğitmenleri, milli servet olan enerjinin verimli kullanılması konusunda küçük yaşlardan itibaren farkındalığı yüksek bir neslin yetişmesi için katkı sağlamayı hedefliyor. Proje 2017-2018 Eğitim Öğretim Yılında 3 bin öğrenciye ulaştı.
</p>

