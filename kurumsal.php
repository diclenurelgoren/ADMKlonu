<p>Kişisel Verilerin İşlenmesi Aydınlatma Metni</p>
<p>ADM Elektrik Dağıtım A.Ş. olarak; müşterilerimiz, çalışanlarımız dahil Şirketimiz ile ilişkili şahısların uhdemizde bulunan kişisel verilerinin 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) ve ilgili mevzuat çerçevesinde; güvence altına alınması ve işlenmesi konusuna hassasiyetle yaklaşmaktayız.</p>

 

<p>Şirketimizin bu konuya göstrdiği hassasiyetle, işbu kişisel verilerin toplanması, kullanımı ve 3. Kişilerle paylaşımı hakkında bildirimi bilgilerinize sunmamız gerekmektedir. Bu çerçevede, KVKK kapsamında Veri Sorumlusu sıfatıyla sizleri aydınlatmak istiyoruz.</p>

 

<p>Kişisel verilerin kullanılması ve korunması başta Anayasa’nın kişisel verilerin korunmasını isteme hakkını düzenleyen 20. Maddesi kapsamında güvence altına alınmış ve KVK Kanunu ile kişisel verilerin, ticari amaçlarla kullanılması düzenlenmiştir.</p>

 

<p>I.Veri Sorumlusu:</p>

 

<p>6698 sayılı Kişisel Verilerin Korunması Kanunu (KVKK) uyarınca, kişisel verileriniz; veri sorumlusu olarak Şirketimiz ile tüketici, abone, müşteri, tedarikçi veya çalışan vb. sıfatlarla paylaştığınız kişisel verileriniz KVKK’ya uygun şekilde, faaliyet ve hizmet amaçlarımız ile bağlantılı ve ölçülü olarak işlenebilecek, işin gerektirmesi halinde işin gerektirdiği kadarıyla üçüncü kişilere aktarılabilecek, saklanacak, profilleme için kullanılabilecek ve sınıflandırılabilecektir.</p>

 

<p>II. Kişisel Verilerin Hangi Amaçla İşleneceği:</p>

 

<p>Şirketimiz ile paylaşmış olduğunuz kişisel verileriniz, Elektrik Piyasası Kanunu uyarınca elektrik dağıtım hizmetinin sunulması, yatırımların yapılması, sayaçların okunması, dağıtım sistemine bağlantı, dağıtım tesislerinin işletilmesi, talep tahminlerinin hazırlanması, dengeleme ve uzlaştırma piyasası, gibi Şirketimizin faaliyet konuları ile ilgili hizmetleri sunabilmek ve bu hizmetlerin kalitesini artırabilmek ve diğer faaliyetlerini yerine getirebilmek ve bilgilendirme yükümlülüklerine uymak, Şirket ve Şirket’le iş ilişkisi içerisinde olan kişilerin hukuki ve ticari güvenliğinin sağlanması, Şirket’in insan kaynakları politikalarının yürütülmesinin temini, ticari ve iş stratejilerinin belirlenmesi ve uygulanması amaçlarıyla KVKK’nın 5 ve 6’ncı maddelerinde belirtilen kişisel veri işleme şartları ve amaçları dâhilinde işlenebilecektir.</p>

 

<p>III. İşlenen Kişisel Verilerin Kimlere ve Hangi Amaçla Aktarılabileceği:</p>

 

<p>Toplanan kişisel verileriniz; iş ortaklarımıza, tedarikçilerimize, ilişki içerisinde bulunulan şirketlere, kanunen yetkili kamu kurumlarına ve özel kişilere KVKK’nın 8 ve 9’uncu maddelerinde belirtilen kişisel veri işleme şartları ve amaçları çerçevesinde aktarılabilecektir.</p>

 

<p>Kişisel verileriniz, yukarıda belirtilen amaçlar dışında kullanılmayacak olup, yasal yükümlülükler ve resmi kurum/kuruluşlar haricinde ilgisiz üçüncü kişilerle paylaşılmayacak ya da ilgisiz üçüncü kişilere aktarılmayacaktır.</p>

 

<p>IV. Kişisel Veri Toplamanın Yöntemi ve Hukuki Sebebi:</p>

 

<p>Kişisel verileriniz, yukarıda belirtilen amaç ve kapsamda sözlü, yazılı veya elektronik olarak toplanabilir.</p>

 

<p>Kişisel verileriniz, Şirketimiz veya Şirketimiz adına veri işleyen gerçek ya da tüzel kişiler tarafından sayılanlarla sınırlı olmamak üzere, internet sitesi, muhtelif sözleşmeler, mobil uygulamalar, elektronik posta, başvuru formları gibi araçlar üzerinden, Şirketimiz ile yapılan yazılı veya sözlü iletişimler vb. kanallar aracılığıyla sözlü, yazılı veya elektronik ortamda toplanmaktadır. Bu doğrultuda toplanan kişisel verileriniz KVKK’nın 5 ve 6’ncı maddelerinde belirtilen kişisel veri işleme şartları ve amaçları kapsamında işbu Aydınlatma Metninde belirtilen amaçlarla da işlenebilmekte ve aktarılabilmektedir.</p>

 

<p>V. Kişisel Verilerin Korunmasına İlişkin Önlemler:</p>

 

<p>Şirket kişisel verilerin korunması konusunda gerekli olan teknik, idari ve hukuki her türlü tedbiri almaktadır. Şirketimiz bu verileri işlerken hukuka ve dürüstlük kuralına riayet ederek verilerin doğru olmasına, işlemin kapsamı dışında kullanılmamasına, meşru amaçlar doğrultusunda işlenmesine, verilerin güncel tutulmasına ve hedeflenen amaç için gereken makul süre kadar muhafaza edilmesine dikkat etmektedir.</p>

 

<p>VI. Kişisel Veri Sahibinin KVKK’nın 11. Maddesinde Sayılan Hakları:</p>

 

<p>Kişisel veri sahipleri olarak, haklarınıza ilişkin taleplerinizi işbu Aydınlatma Metni’nde aşağıda düzenlenen yöntemlerle Şirket’e iletmeniz durumunda talebin değerlendirilmesini müteakip en kısa sürede ve en geç 30 gün içinde tarafınıza dönüş yapılacaktır.</p>

 

<p>Bu kapsamda kişisel veri sahipleri;<p>

