<div class="hizmet">HİZMETLER İÇİN TIKLAYINIZ</div>
<div class="panel1">
<p>Yatırımlar
ADM Elektrik Dağıtım sanayi ve turizm cenneti olarak hızla büyüyen Aydın, Denizli ve Muğla illerinde elektriğin kalite, yeterlilik ve sürekliliğinin sağlanması için altyapının güçlendirilmesi için yatırımlara hızla devam etmektedir.</p>

<p>Yıllara göre Yatırım Miktarı</p>

<p>ADM Elektrik Dağıtım, göreve başladığı 2008 yılından 2010 yılına kadar, günümüz fiyatları ile yaklaşık 130 Milyon TL, 2011-2015 yıllarını kapsayan 5 yıllık süreçte yaklaşık 800 Milyon TL yatırım yapmıştır. 2016-2020 dönemi için ise bölgede yaklaşık 1 Milyar TL yatırım yapılması hedeflemektedir. Yatırımlar içerisinde büyük oranda yeni yeraltı elektrik şebekeleri ve trafolar, yenileme-iyileştirme çalışmaları ve teknolojik yatırımlar yer almaktadır. Bu yeni yatırımlarla mevcut ve yeni tüketicilere sağlanan enerji kalitesi sürekli artmaktadır.</p>

<p>Yatırım Planlama Stratejisi</p>

<p>ADM Elektrik Dağıtım yatırım planlama stratejisinde, tüketicinin günümüz koşullarının yanı sıra gelecekteki beklentisi ve ihtiyaçları da dikkate alınarak elektrik şebekesinin teknik ve ekonomik performansını artıracak adımlar öngörülmektedir. ADM Elektrik Dağıtım yatırımlarında teknoloji ve inovasyon odaklı çalışmaların önceliklendirilmesi, her aşamada kalite standartlarının yükseltilmesi, uluslararası standartta teknolojik malzeme kullanılması, bilgili ve tecrübeli kalifiye personelin istihdam edilmesi ve bunların doğal sonucu olarak işletme verimliliğinin arttırılması gözetilmektedir.</p>

<p>Uzun Vadeli Yatırım Tasarımı</p>

<p>Elektrik dağıtım şebekelerinin ömrünün 25- 30 yıl olduğu ve yapım maliyetlerinin yüksek olduğu dikkate alındığında uzun ömürlü bir tasarımın gerekliliği ortaya çıkmaktadır.  ADM Elektrik Dağıtım bu gerekliliği karşılamak üzere uzun vadeli planlama çalışmalarını özenle sürdürmektedir.</p>

<p>Mevcut elektrik dağıtım şebekesi, uzaktan izleme ve kontrol sistemleri ile analiz edilmekte ayrıca tüketici talepleri, oluşacak yeni yerleşim bölgeleri, Kurum ve Kuruluşların ileri vadede planları ve diğer etkenler bir araya getirilerek, bu veriler master plan yazılımları yardımıyla modellenmekte ve en doğru sonucu verecek şekilde yatırım planları hazırlanmaktadır. Bu sayede 20-30 yıl gibi uzun vadeli elektrik dağıtım şebekeleri tasarlanmaktadır.</p>

<p>Ekonomik ömrünü tamamlamış elektrik dağıtım tesisleri sürekli alınan veriler ile tespit edilerek, bu tesislerde yine uzun vadeli planlama ile yenileme ve iyileştirme çalışmaları gerçekleştirilmektedir.</p> 

<p>Yapılan tüm dağıtım şebekesi tasarımları ve planlamaları, sadece bugünü değil 20-30 yıl sonrasını karşılayabilecek şekilde hazırlanmaktadır. Bu planlamalarla ADM’nin hedefi, Ülkemiz kaynaklarını en verimli şekilde kullanarak tüketicilere elektrik enerjisinin sağlanmasıdır.<p>

<p>Önümüzdeki 5 yıl içerisinde öngörülen yatırım ile elektriksel şebeke modellemesinin geliştirilmesi, ADM Elektrik Dağıtım bünyesinde kullanılan diğer sistemler tüm entegrasyon çalışmalarının tamamlanarak dağıtım şebekesinin elektronik ortamda güncel kalması sağlanılacaktır.<p>

